package Package.java;

public class Student
{
	private String ID;
	private String Name;
	private int age;
	
	public Student (String ID, String Name)
	{
		this.ID = ID;
		this.Name = Name;
	}
	
	public void setage (int age)
	{
		this.age = age;
	}
	
	public int getage (int age)
	{
		return age;
	}
	
	
	
}
